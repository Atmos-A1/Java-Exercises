public class MultiplicationBy5{
		public static void main(String[]args){

		int number = 5;
		int multiple = 0;

		for(int firstnumber = 1; firstnumber <= 12; firstnumber++){
			multiple = number * firstnumber;
			
		System.out.printf("The multiplication of %d and %d is %d%n", number, firstnumber, multiple);
		
		}

		}
	}
			
